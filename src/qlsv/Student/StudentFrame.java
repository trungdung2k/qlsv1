    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
                int selectedIndex = tblStudent.getSelectedRow();
        if(selectedIndex >= 0) {
            Student std = studentList.get(selectedIndex);
        int id = std.getId();
        String fullname = txtFullname.getText();
        String msv = txtMsv.getText();
        String gender = cbGender.getSelectedItem().toString();
        int age = Integer.parseInt(txtAge.getText());
        String phone = txtPhone.getText();
        String address = txtAddress.getText();
        String email = txtEmail.getText();
        
        Student stdudent = new Student( id , fullname,  msv,  gender,  phone,  address,  email,  age );     
        StudentModify.update(stdudent);
        showStudent();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
            int selectedIndex = tblStudent.getSelectedRow();
            if(selectedIndex >= 0) {
            Student std =  studentList.get(selectedIndex);
            txtFullname.setText(std.fullname);
            txtMsv.setText(std.msv);
            cbGender.setSelectedItem(std.gender.toString());
            txtAge.setText(Integer.toString(std.age));
            txtPhone.setText(std.phone);
            txtAddress.setText(std.address);
            txtEmail.setText(std.email);
//           
        }

    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        int selectedIndex = tblStudent.getSelectedRow();
        if(selectedIndex >= 0) {
            Student std = studentList.get(selectedIndex);
            
            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this item?");             
            if(option == 0) {
                StudentModify.delete(std.getId());                
                showStudent();
            }
        }     
    }//GEN-LAST:event_btnDeleteActionPerformed

