    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String fullname = txtFullname.getText();
        String msv = txtPhone.getText();
        String gender = cbGender.getSelectedItem().toString();
        int age = Integer.parseInt(txtAge.getText());
        String phone = txtPhone.getText();
        String address = txtAddress.getText();
        String email = txtEmail.getText();
        
        Student std = new Student( fullname,  msv,  gender,  phone,  address,  email,  age);
        
        StudentModify.insert(std);
        
        showStudent();
        
        resetFieldForm();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void resetFieldForm () {
            txtFullname.setText("");
            txtMsv.setText("");
            cbGender.setSelectedItem("Nam");
            txtAge.setText("");
            txtPhone.setText("");
            txtAddress.setText("");
            txtEmail.setText("");
    }
    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
    String input = JOptionPane.showInputDialog(this, "Enter msv to search");
        if(input != null && input.length() > 0) {
            studentList = StudentModify.findByMsv(input);
            
            tableModel.setRowCount(0);
        
           studentList.forEach((student) -> {
            tableModel.addRow(new Object[] {tableModel.getRowCount() + 1, student.getFullname(), student.getMsv(),
                student.getGender(),student.getAge(),student.getPhone(),student.getAddress(), student.getEmail()});
            });
        } else {
            showStudent();
        }  
    }//GEN-LAST:event_btnFindActionPerformed

